
function login(){
    const signup = document.getElementById("sign");
    const login = document.getElementById("log");
    login.style.left = 0
    signup.style.left = '170%'
}





function signup(){
    const signup = document.getElementById("sign");
    const login = document.getElementById("log");
    login.style.left = '-150%'
    signup.style.left = '170%';
    signup.style.width = "50%"
}

